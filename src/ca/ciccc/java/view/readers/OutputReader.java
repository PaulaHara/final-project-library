package ca.ciccc.java.view.readers;

/**
 * Class InputReader reads user input from the keyboard.
 */

/**
 * @author paula on 23/04/18.
 */
public class OutputReader {

    /**
     * Print the string passed
     */
    public void printOutput(String toPrint) {
        System.out.println(toPrint);
    }

    /**
     * Print the string passed on the same line
     */
    public void printOutputInLine(String toPrint) {
        System.out.print(toPrint);
    }
}
